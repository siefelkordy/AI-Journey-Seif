i = 1
while i <= 10:
    print(i)
    i += 1

print("Loop finished.")
# This code will print numbers from 1 to 10 using a while loop.
# The loop continues as long as the condition (i <= 10) is true.
# The variable i is incremented by 1 in each iteration.
# The loop will stop when i becomes 11, at which point the condition is no longer true.
